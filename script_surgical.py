"""script_surgical.py — ТОЧЕЧНАЯ правка сценария (адаптация carousel.surgical_edit).

Sonnet получает текущий сценарий + инструкцию автора → возвращает версию с ТОЛЬКО
запрошенным изменением, остальное байт-в-байт. В отличие от edit_mode/
`_apply_script_instruction` (которые РЕГЕНЕРЯТ текст), здесь — минимальная правка
слова/знака без переписывания.

Детект no-op: если Sonnet вернул то же самое (не нашёл, что менять) — SurgicalNoOp,
обработчик просит переформулировать. Реюз парсера «X поменяй на Y» из carousel.
"""
from __future__ import annotations

import re

from carousel.llm import _extract_replace_pattern  # «X поменяй на Y» → (X, Y), реюз

_SURG_SCRIPT_SYSTEM = """Ты — редактор сценариев коротких вертикальных роликов.
Тебе дают текущий сценарий и инструкцию автора (живая русская речь). Внеси
ТОЧЕЧНУЮ правку строго по инструкции, сохранив всё остальное БАЙТ-В-БАЙТ.

ПРАВИЛА:
1. НЕ переписывай и НЕ перестраивай сценарий. Меняй ТОЛЬКО то, что просит
   инструкция: слово, знак препинания, короткую фразу.
2. «<X> поменяй на <Y>» / «замени <X> на <Y>» / «<X> → <Y>» — найди подстроку
   <X> (нормализуя пробелы) и замени на <Y>, остальной текст сохрани дословно.
3. «убери лишнюю запятую/знак», «исправь опечатку в слове Z», «убери пару букв»
   — правь только это место, ничего больше.
4. Если не можешь найти место правки / применить инструкцию — верни сценарий
   БЕЗ ИЗМЕНЕНИЙ (это сигнал автору переформулировать).
5. НЕ добавляй пояснений, префикса «СЦЕНАРИЙ:», markdown. Верни ТОЛЬКО итоговый
   текст сценария."""


class SurgicalNoOp(Exception):
    """Sonnet вернул сценарий без изменений (не нашёл / не применил правку)."""


def _norm(s: str) -> str:
    """Нормализация пробелов для сравнения «изменилось ли»."""
    return re.sub(r"\s+", " ", (s or "")).strip()


def surgical_edit_script(
    claude, script: str, instruction: str,
    model: str = "claude-sonnet-4-6", max_tokens: int = 2048,
) -> str:
    """Применяет ТОЧЕЧНУЮ правку к сценарию → изменённый текст.

    Бросает SurgicalNoOp, если правка ничего не изменила (нечего менять / Sonnet
    не нашёл подстроку) — сигнал автору переформулировать.
    """
    user_msg = (
        f"ТЕКУЩИЙ СЦЕНАРИЙ:\n\n{script}\n\n"
        f"━━━━━━━━━━━━━━━━━━\n"
        f"ИНСТРУКЦИЯ АВТОРА:\n«{instruction}»\n\n"
        f"Верни весь сценарий с ТОЛЬКО запрошенной правкой. Остальное — байт-в-байт."
    )
    resp = claude.messages.create(
        model=model, max_tokens=max_tokens,
        system=_SURG_SCRIPT_SYSTEM,
        messages=[{"role": "user", "content": user_msg}],
    )
    raw = (resp.content[0].text if resp.content else "").strip()
    edited = raw
    if edited.upper().startswith("СЦЕНАРИЙ"):
        edited = edited.split("\n", 1)[-1].strip()

    # No-op: Sonnet вернул то же. Сравниваем И сырой ответ — на случай, если
    # сам сценарий начинался с «СЦЕНАРИЙ» и срез выше создал бы ложную «правку».
    if not edited or _norm(edited) == _norm(script) or _norm(raw) == _norm(script):
        rp = _extract_replace_pattern(instruction)
        hint = f" Не нашёл в тексте: «{rp[0]}»." if rp else ""
        raise SurgicalNoOp(
            f"Точечная правка ничего не изменила.{hint} "
            f"Переформулируй или используй «✏️ Внести правки» (перегенерация)."
        )
    return edited
