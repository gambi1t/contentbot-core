def _polish_post_finale(
    draft: str,
    claude_client,
    brand: str,
    *,
    max_tokens: int = 1500,
) -> str:
    """Second pass: polish the finale and fix orphan sentences.

    Mirrors Artem's `_force_shorten` pattern (second Claude pass with a
    narrow editorial task). Cheap — Sonnet is enough for this kind of
    surface fix, and faster than another Opus run.

    Returns the polished post on success; falls back to the original draft
    if Claude fails or returns something obviously wrong (too short, empty).
    """
    if not draft or not draft.strip():
        return draft

    system = _POLISH_SYSTEM_MAKSIM if brand == "maksim" else _POLISH_SYSTEM_ARTEM

    # Brand-aware задача polish: для Артёма (default) — РАЗРЕШАЕМ переписать тело
    # по анти-машинному стоп-листу системного промпта; для Максима — узко, как было.
    polish_task = (
        "Это пост Артёма — тело трогать можно и нужно. Пройди весь текст и "
        "перепиши машинные обороты в его живой голос по правилам системного "
        "промпта, оставив факты, цифры, порядок блоков и длину (±10%) на месте. "
        "Машинный оборот важнее обрыва строки: если абзац звучит как генератор "
        "текста — перепиши его целиком в речь Артёма, а не латай по краям. "
        "Только не выглаживай до стерильности и ничего не выдумывай. "
        "Заодно поправь обрывы фраз и усиль финал (мостик). "
        "Верни только готовый текст."
    ) if brand != "maksim" else (
        "Поправь обрывы фраз и усилии финал (мораль/образ/"
        "мостик). Тело и заголовок не трогай. "
        "Верни только готовый текст."
    )

    # Бюджет токенов: у Артёма polish переписывает ВСЁ тело → выход ≈ длине
    # входа. Дефолт 1500 обрезал бы длинный пост (эталон-камбэк ~1920 симв,
    # review до 1800) → потеря хвоста с фактами. Считаем под длину черновика
    # с запасом (кириллица ≤~1 симв/токен). Максиму (узкая правка финала)
    # дефолта хватает — не трогаем.
    if brand != "maksim":
        effective_tokens = min(4000, max(max_tokens, len(draft) + 600))
    else:
        effective_tokens = max_tokens

    try:
        response = claude_client.messages.create(
            model="claude-sonnet-4-6",
            max_tokens=effective_tokens,
            system=system,
            messages=[
                {
                    "role": "user",
                    "content": (
                        f"Черновик поста:\n\n{draft}\n\n" + polish_task
                    ),
                },
            ],
        )
        # Обрыв по лимиту токенов → НЕ отдаём усечённый текст: страховка <0.5
        # ловит только сильную обрезку, а обрыв на 60-75% прошёл бы её и унёс
        # хвост поста с фактами/цифрами. Безопасно возвращаем исходный черновик.
        if getattr(response, "stop_reason", None) == "max_tokens":
            return draft
        polished = response.content[0].text.strip()
        # Clean any code-fence wrap or "Вот пост:" prefix
        polished = re.sub(r"^```(?:markdown)?|```$", "", polished, flags=re.MULTILINE).strip()
        if polished.lower().startswith("вот пост"):
            polished = polished.split("\n", 1)[-1].strip()
        # Sanity: too-short or empty → keep draft
        if len(polished) < len(draft) * 0.5:
            return draft
        return polished
    except Exception:
        # Best-effort polish — never crash the pipeline because of it
        return draft


