def generate_post(inp: PostInput, claude_client, *, max_tokens: int = 1500,
                  brand: str | None = None) -> str:
    """
    Генерирует готовый Telegram-пост по спеке.

    claude_client — инстанс anthropic.Anthropic(api_key=...).
    Модель жёстко зашита: claude-opus-4-7 (Sonnet не подходит — нужна тонкая
    работа с тоном).

    brand — явный бренд ("maksim"/"default"/...). Если None — резолвится
    через bot._get_active_brand_name() лениво (lazy import чтобы не было
    circular: tg_post_writer импортируется до bot во многих случаях).
    """
    user_msg = _build_user_message(inp)

    if brand is None:
        # Lazy import: tg_post_writer is imported by bot.py via register_tgpost,
        # so a top-level import would loop. Catch the *narrow* set of errors
        # that mean "bot module isn't ready yet" — anything else (e.g. a real
        # bug in _get_active_brand_name) should propagate so we see it in logs.
        try:
            from bot import _get_active_brand_name
            brand = _get_active_brand_name()
        except (ImportError, AttributeError):
            brand = "default"

    # Runtime guard: review_list / review_essay / thesis are Maksim-specific
    # post types — their user messages reference Maksim's biography and lists.
    # If you call them on Artem's brand, the system prompt is Artem-specific
    # but the user message is Maksim-specific → output is incoherent. Fail
    # loudly at the call site instead of producing a garbage post.
    if brand != "maksim" and inp.post_type in MAKSIM_ONLY_POST_TYPES:
        raise ValueError(
            f"post_type={inp.post_type!r} is Maksim-only; "
            f"called with brand={brand!r}. Use brand='maksim' or pick "
            f"a post_type from intro/stage/video_companion/thought."
        )

    response = claude_client.messages.create(
        model="claude-opus-4-7",
        max_tokens=max_tokens,
        system=_get_system_prompt(brand),
        messages=[{"role": "user", "content": user_msg}],
    )
    text = response.content[0].text.strip()

    # Страховка: срежем возможные обрамления «вот пост:» или ``` если модель ослушалась.
    if text.lower().startswith("вот пост"):
        text = text.split("\n", 1)[-1].strip()
    if text.startswith("```"):
        text = text.strip("`").strip()
        if text.startswith("markdown"):
            text = text[len("markdown"):].strip()

    # ═══ Second pass: polish the finale + fix orphan sentences ═══
    # Per Artem's feedback (8 May 2026): the first Opus pass occasionally
    # produces orphan fragments like "Речка — это сезон" (no predicate)
    # and riddle-finales like "Землю выбираешь один раз. Вырубленный лес
    # обратно не растёт" (image without resolution). A narrow Sonnet pass
    # fixes both, brand-aware. Best-effort — never crashes pipeline.
    text = _polish_post_finale(text, claude_client, brand or "default")

    return text


