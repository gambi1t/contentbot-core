PostType = Literal["intro", "stage", "video_companion", "thought"]
# intro           — вводный пост на канал (один на канал, без номера)
# stage           — пост-этап эксперимента (Этап N), без видео
# video_companion — длинный пост в Telegram-канал к уже существующему ролику.
#                   Ролик будет приложен к посту, но текст должен стоять сам по себе:
#                   читатель понимает смысл даже не смотря видео.
# thought         — короткий свободный пост: мысль, наблюдение, вывод. Без
#                   привязки к этапу или ролику. 2–4 абзаца, 300–700 знаков.


@dataclass
class PostInput:
    post_type: PostType                 # "intro" | "stage" | "video_companion"
    stage_num: Optional[int] = None     # для stage: 1, 2, 3, ...
    facts: str = ""                     # фактура этапа / сути видео
    bridge_from_previous: str = ""      # для stage — мостик из предыдущего поста
    extra_notes: str = ""               # опциональные уточнения (настроение, акцент)
    # Для video_companion:
    video_script: str = ""              # сценарий ролика (озвучка)
    short_description: str = ""         # короткое описание (200 симв) для других площадок, если уже есть
    video_topic: str = ""               # о чём ролик одной фразой


# ═══ GENERATE ════════════════════════════════════════════════════════════════

def _build_user_message(inp: PostInput) -> str:
    if inp.post_type == "intro":
        parts = [
            "Тип поста: ВВОДНЫЙ (без номера этапа).",
            "",
            "Фактура (что нужно упаковать в пост):",
            inp.facts.strip() or "(фактура не задана — используй дефолтные параметры эксперимента)",
        ]
    elif inp.post_type == "stage":
        if inp.stage_num is None:
            raise ValueError("stage_num обязателен для post_type='stage'")
        parts = [
            f"Тип поста: ЭТАП {inp.stage_num}.",
            "",
            "Фактура этапа (что произошло, какие числа, события, инструменты):",
            inp.facts.strip() or "(фактура пустая — сообщи пользователю и попроси наполнить)",
            "",
            "Мостик из предыдущего поста (что было обещано):",
            inp.bridge_from_previous.strip() or "(мостика нет — начни этап с нулевой точки)",
        ]
    elif inp.post_type == "video_companion":
        # Длинный пост для Telegram-канала к существующему ролику.
        # Видео = приложение, пост = самостоятельная единица.
        parts = [
            "Тип поста: СОПРОВОДИТЕЛЬНЫЙ К ВИДЕО (длинный пост для Telegram-канала).",
            "",
            "ВАЖНО: к посту будет приложен ролик (30 сек, AI-аватар Артёма). "
            "Но пост должен стоять САМ ПО СЕБЕ — читатель канала понимает смысл, даже "
            "не смотря видео. Для других площадок (Insta/YT/TikTok/VK) идёт короткое "
            "описание и ролик — там пост не нужен. Здесь — наоборот: текст главный, "
            "видео иллюстрирует.",
            "",
            "Структура (используй как опору, не жёстко):",
            "— Заголовок жирным первой строкой",
            "— Абзац-крюк: наблюдение или парадокс (1–3 предложения)",
            "— 2–4 абзаца сути с конкретикой: что, цифры, контекст",
            "— (опц.) короткая ремарка «ниже — ролик на эту тему, если удобнее смотреть»",
            "— Мостик/панчлайн в конце",
            "",
            "Тема ролика одной фразой:",
            inp.video_topic.strip() or "(не указана — выведи из сценария ниже)",
            "",
            "Сценарий ролика (текст озвучки, 30 сек):",
            inp.video_script.strip() or "(сценарий пустой — попроси пользователя указать)",
        ]
        if inp.short_description.strip():
            parts.extend([
                "",
                "Короткое описание, которое бот сгенерил для Insta/YT/TikTok (для справки, "
                "чтобы понимать, о чём видео):",
                inp.short_description.strip(),
            ])
        parts.extend([
            "",
            "Длина поста — 600–1200 знаков. Больше обычного этапа, но не полотно. "
            "Это не расшифровка сценария — это авторская подача с личным взглядом.",
        ])
    elif inp.post_type == "thought":
        # Короткий свободный пост — наблюдение, мысль, парадокс.
        # Самостоятельный, без номера этапа и без ролика.
        parts = [
            "Тип поста: МЫСЛЬ / НАБЛЮДЕНИЕ (короткий свободный пост).",
            "",
            "ВАЖНО: это НЕ этап эксперимента и НЕ сопровождение ролика. "
            "Это короткое самостоятельное наблюдение. Без номера, без "
            "обязательного мостика в следующий пост, без арифметических блоков.",
            "",
            "Структура (мягкая опора, можно гнуть):",
            "— Заголовок жирным первой строкой — короткий, 2–4 слова, как "
            "заголовок заметки. Не «Этап N», не «Мысль дня».",
            "— Абзац-крюк: сама мысль / наблюдение / парадокс в 1–2 предложениях.",
            "— 1–3 абзаца раскрытия: где это заметил, откуда взялось, какой контекст.",
            "— Заключительная строка: короткий вывод-констатация. НЕ мостик в "
            "следующий пост, НЕ призыв. Просто точка, после которой хочется помолчать.",
            "",
            "Длина 300–700 знаков. Короче этапа, короче сопровода. "
            "Читается за 20 секунд с телефона.",
            "",
            "Тон: те же правила, что всегда — чистый язык, взрослый юмор, без "
            "штампов и без «для клиентов автоматизировано, себе руками». Шутка "
            "работает в этом конкретном абзаце или вырезается.",
            "",
            "Фактура (мысль, о которой пишем):",
            inp.facts.strip() or "(фактура пустая — сообщи пользователю и попроси наполнить)",
        ]
    else:
        raise ValueError(f"Неизвестный post_type: {inp.post_type!r}")

    if inp.extra_notes.strip():
        parts.extend(["", "Дополнительные заметки:", inp.extra_notes.strip()])

    parts.extend([
        "",
        "Напиши пост, строго следуя правилам тона из system-промпта. "
        "Не добавляй пояснений — только готовый текст поста.",
    ])
    return "\n".join(parts)


def generate_post(inp: PostInput, claude_client, *, max_tokens: int = 1500) -> str:
    """
    Генерирует готовый Telegram-пост по спеке.

    claude_client — инстанс anthropic.Anthropic(api_key=...).
    Модель жёстко зашита: claude-opus-4-20250514.
    """
    user_msg = _build_user_message(inp)

    response = claude_client.messages.create(
        model="claude-opus-4-20250514",
        max_tokens=max_tokens,
        system=SYSTEM_PROMPT,
        messages=[{"role": "user", "content": user_msg}],
    )
    text = response.content[0].text.strip()

    # Страховка: срежем возможные обрамления «вот пост:» или ``` если модель ослушалась.
    if text.lower().startswith("вот пост"):
        text = text.split("\n", 1)[-1].strip()
    if text.startswith("```"):
        text = text.strip("`").strip()
        if text.startswith("markdown"):
            text = text[len("markdown"):].strip()

    return text


